primerNumero = int(input("Ingrese un numero: "))
segundoNumero = int(input("Ingrese otro numero: "))

print(f"La suma de {primerNumero} y {segundoNumero} es {primerNumero + segundoNumero}")
print(f"La resta de {primerNumero} y {segundoNumero} es {primerNumero - segundoNumero}")
print(f"La multiplicacion de {primerNumero} y {segundoNumero} es {primerNumero * segundoNumero}")
print(f"La division de {primerNumero} y {segundoNumero} es {int(primerNumero / segundoNumero)}")